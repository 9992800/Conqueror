package com.jctx.chat.module;

import java.sql.Date;

public class FriendsApplication {
	public static int APPLY_STATUS_BEING  = 0;
	public static int APPLY_STATUS_PASS   = 1;
	public static int APPLY_STATUS_REJECT = 2;

	int fromuid, touid, status;
	Date createTime, updateTime;
	
	
	public int getFromuid() {
		return fromuid;
	}
	public void setFromuid(int fromuid) {
		this.fromuid = fromuid;
	}
	public int getTouid() {
		return touid;
	}
	public void setTouid(int touid) {
		this.touid = touid;
	}
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}
	public Date getCreateTime() {
		return createTime;
	}
	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}
	public Date getUpdateTime() {
		return updateTime;
	}
	public void setUpdateTime(Date updateTime) {
		this.updateTime = updateTime;
	}
	
	
	@Override
	public String toString() {
		return "FriendsApplication [fromuid=" + fromuid + ", touid=" + touid + ", status=" + status + ", createTime="
				+ createTime + ", updateTime=" + updateTime + "]";
	}
	
}
